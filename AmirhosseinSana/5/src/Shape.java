/**
 * The type Shape.
 */
public abstract class Shape {
    /**
     * Calculate perimeter double.
     *
     * @return the double
     */
    public abstract double calculatePerimeter();

    /**
     * Calculate area double.
     *
     * @return the double
     */
    public abstract double calculateArea();

    /**
     * Draw.
     */
    public abstract void draw();
}
